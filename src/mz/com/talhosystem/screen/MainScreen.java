    private void search_custum() {
        String sql = "SELECT codserv AS Codigo, nameclient as Cliente ,date as Data FROM prices  WHERE nameclient like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtPesq.getText() + "%");
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

