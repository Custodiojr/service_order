    //============================================= set field when click on the table ========================================
    private void set_fields() {
        int setar = jTable1.getSelectedRow();
        jTextField8.setText(jTable1.getModel().getValueAt(setar, 0).toString());

        String sql = "SELECT pl.product AS Produto, pl.qt AS Quantidade , pl.buy AS Compra , pl.sell AS Venda FROM produtslist AS pl WHERE pl.sercod = ? ";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, jTextField8.getText());
            rs = pst.executeQuery();
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));

            String sql1 = "SELECT p.totqty, p.totbuy, p.totsell, p.profit FROM prices AS p WHERE p.codserv = ?";
            try {
                pst = conexao.prepareStatement(sql1);
                pst.setString(1, jTextField8.getText());
                rs = pst.executeQuery();
                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Product code not found");
                } else {
                    String defoult = "###,###.##";
                    DecimalFormat df = new DecimalFormat(defoult);
                    Double totqty = rs.getDouble("totqty");
                    Float totbuy = rs.getFloat("totbuy");
                    Float totsell = rs.getFloat("totsell");
                    Float profit = rs.getFloat("profit");

                    totkg.setText(Double.toString(totqty));

                    totbuyy.setText(df.format(totbuy));
                    totselll.setText(df.format(totsell));
                    profitt.setText(df.format(profit));
                }

            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, e);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

