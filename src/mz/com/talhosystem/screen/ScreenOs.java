    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtNameCli.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insere o nome do clinte");
        } else {

            // Insert rows to database
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();

            try {
                for (int i = 0; i < model.getRowCount(); i++) {


                    String product = model.getValueAt(i, 0).toString();
                    double qty = Double.valueOf(model.getValueAt(i, 1).toString());
                    int buy = Integer.valueOf(model.getValueAt(i, 2).toString());
                    int sell = Integer.valueOf(model.getValueAt(i, 3).toString());
                    int serviceCode = Integer.parseInt(txtOvenda.getText());

                    //insert into a database produtslist
                    String sql = "INSERT INTO produtslist(product,qt,sercod,buy,sell) VALUES('" + product + "','" + qty + "','" + serviceCode + "','" + buy + "','" + sell + "')";
                    pst = conexao.prepareStatement(sql);

                    pst.executeUpdate();

                }

                //Get values from de textfields
                int serviceCodePrices = Integer.parseInt(txtOvenda.getText());
                int profit = Integer.parseInt(txtlucro.getText().replaceAll(" ", ""));
                double totqty = Double.parseDouble(txtTotkg.getText());
                int totbuy = Integer.parseInt(totSpentt.getText().replaceAll(" ", ""));
                int totsell = Integer.parseInt(txtTotVenda.getText().replaceAll(" ", ""));
                String nameClient = txtNameCli.getText();
                //insert into a database prices
                String sql = "INSERT INTO prices(codserv,totsell,totbuy,totqty,profit,nameclient) VALUES(?,?,?,?,?,?)";
                pst = conexao.prepareStatement(sql);
                pst.setInt(1, serviceCodePrices);
                pst.setInt(2, totsell);
                pst.setInt(3, totbuy);
                pst.setDouble(4, totqty);
                pst.setInt(5, profit);
                pst.setString(6, nameClient);
                int add = pst.executeUpdate();
                if (add > 0) {
                    JOptionPane.showMessageDialog(null, "Operação feita com sucesso!");
                    clean();

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed
