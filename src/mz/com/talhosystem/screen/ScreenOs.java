    private void btnRemovActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemovActionPerformed
        // Desenabled the buttons
        String defoult = "###,###.##";
        DecimalFormat df = new DecimalFormat(defoult);
        btnSave.setEnabled(true);
        btnGen.setEnabled(true);
        btnAdd.setEnabled(true);
        btnRemov.setEnabled(false);
        //Remove the selected row
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) jTable1.getModel();
        model.removeRow(jTable1.getSelectedRow());
        //discount weigth(kg),spent amount,total sell and profit after  remove row
        double qty = Double.parseDouble(totalkg.getText());
        double totkg = Double.parseDouble(txtTotkg.getText());
        int totalSpentTable = Integer.parseInt(totalgasto.getText());
        int totalSpentField = Integer.parseInt(totSpentt.getText().replaceAll(" ", ""));

        int totalSellField = Integer.parseInt(txtTotVenda.getText().replaceAll(" ", ""));
        int totalSellTable = Integer.parseInt(totvenda.getText());

        int totSell = totalSellTable - totalSellField;
        int totSpent = totalSpentField - totalSpentTable;

        qty = qty - totkg;
        if (qty < 0) {
            qty = qty * (-1);
        }
        if (totSell < 0) {
            totSell = totSell * (-1);
        }
        if (totSpent < 0) {
            totSpent = totSpent * (-1);
        }

        txtTotkg.setText(Double.toString(qty));
        totSpentt.setText(df.format(totSpent));
        txtTotVenda.setText(df.format(totSell));
        int profit = totSell - totSpent;
        if (profit < 0) {
            profit = profit * (-1);
        }
        txtlucro.setText(df.format(profit));


    }//GEN-LAST:event_btnRemovActionPerformed

