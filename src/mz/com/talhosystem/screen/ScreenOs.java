    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtPcompra.getText().isEmpty() || jComboBox1.getSelectedItem().equals("Selecione") || spinqty.getText().isEmpty() || txtPcompra.getText().isEmpty() || txtPvenda.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos campos!");
        } else {
            String defoult = "###,###.##";
            DecimalFormat df = new DecimalFormat(defoult);
            // Add data to the table
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jTable1.getModel();

            model.addRow(new Object[]{
                jComboBox1.getSelectedItem(),
                spinqty.getText(),
                txtPcompra.getText(),
                txtPvenda.getText(),});

            double qty = 0;
            int totSpent = 0;
            int totSel = 0;
            int profit;
            for (int i = 0; i < jTable1.getRowCount(); i++) {
               // qty = qty + Integer.parseInt(jTable1.getValueAt(i, 1).toString());
                qty = Double.sum(qty, Double.parseDouble(jTable1.getValueAt(i, 1).toString())) ; 
                totSpent = totSpent + Integer.parseInt(jTable1.getValueAt(i, 2).toString());
                totSel = totSel + Integer.parseInt(jTable1.getValueAt(i, 3).toString());
            }

            txtTotkg.setText(Double.toString(qty));

            totSpentt.setText(df.format(totSpent));

            txtTotVenda.setText(df.format(totSel));
            profit = totSel - totSpent;
            if (profit < 0) {
                profit = profit * (-1);
            }
            txtlucro.setText(df.format(profit));
            txtPcompra.setText("");
            txtPvenda.setText("");
            spinqty.setText("");
            jComboBox1.setSelectedItem("Selecione");
            jComboBox1.requestFocus();
            btnSave.setEnabled(true);
        }

    }//GEN-LAST:event_btnAddActionPerformed

